// -------------------------------------------------------------------------------------------------------------------------
// Import
import * as circuit from "../../js/circuit.mjs";

// -------------------------------------------------------------------------------------------------------------------------
// Register
circuit.registerComponent({
	name: "nand",
	description: "Nand gate",
	version: "1.0.0.0",
	create: () => new Nand()
});

// -------------------------------------------------------------------------------------------------------------------------
// Implementation
class Nand
{
	constructor()
	{
		this.inputs = [ false, false ];
		this.outputs = [false];
	}

	update()
	{
		this.outputs[0] = !(this.inputs[0] && this.inputs[1]);
	}
}

